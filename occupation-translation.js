function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function applyOrderedMap(input, entries) {
  let output = input;
  entries.forEach(([source, target]) => {
    output = output.replace(new RegExp(escapeRegExp(source), "gi"), target);
  });
  return output;
}

function normalizeTitle(title) {
  return String(title || "").replace(/\s+/g, " ").trim();
}

function cleanupTranslatedTitle(text) {
  return text
    .replace(/\s*&\s*/g, "与")
    .replace(/\s*\/\s*/g, "/")
    .replace(/\s*,\s*/g, "、")
    .replace(/\bAnd\b/gi, "与")
    .replace(/\band\b/gi, "与")
    .replace(/\s+-\s+/g, "-")
    .replace(/\s+/g, "")
    .replace(/、与/g, "、")
    .replace(/与、/g, "与")
    .replace(/与与/g, "与")
    .replace(/类类/g, "类")
    .replace(/岗岗/g, "岗")
    .trim();
}

function hasChinese(text) {
  return CJK_RE.test(String(text || ""));
}

export function needsOccupationZhTitle(title, titleZh) {
  const zh = String(titleZh || "").trim();
  return !zh || zh === String(title || "").trim() || !hasChinese(zh);
}

export function translateOccupationTitle(title) {
  const normalized = normalizeTitle(title);
  if (!normalized) return normalized;
  if (EXACT_TITLE_MAP.has(normalized)) return EXACT_TITLE_MAP.get(normalized);

  let translated = applyOrderedMap(normalized, PHRASE_MAP);

  Array.from(TOKEN_MAP.entries())
    .sort((a, b) => b[0].length - a[0].length)
    .forEach(([source, target]) => {
      translated = translated.replace(new RegExp(`\\b${escapeRegExp(source)}\\b`, "gi"), target);
    });

  translated = cleanupTranslatedTitle(translated);
  return translated || normalized;
}

export function withTranslatedOccupationTitle(row) {
  if (!row) return row;
  if (!needsOccupationZhTitle(row.title, row.titleZh)) return row;
  return {
    ...row,
    titleZh: translateOccupationTitle(row.title)
  };
}
